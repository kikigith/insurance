package net.itdeal.ins.entities;
/**
 * Class CertifiedOrganization defined 
 * any certified institution it may be : hospital, 
 * drugstore and alike
 * @author kiki
 *
 */

public class CertifiedOrganization {
	private Long id;
	private String name;
	private String description;
	private String phone;
	private String address;
	private COType coType;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public COType getCoType() {
		return coType;
	}
	public void setCoType(COType coType) {
		this.coType = coType;
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		CertifiedOrganization other = (CertifiedOrganization) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}
	
	

}
